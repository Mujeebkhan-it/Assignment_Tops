//3. What is the difference between OOP and POP?

//OOP (Object-Oriented Programming):
//		
//		Centers around the concept of "objects" which are instances of classes. 
//		Objects combine data (attributes) and methods (functions) that operate on the data.
//		
//		Emphasizes modeling real-world entities and their interactions. 
//		It aims for encapsulation, abstraction, inheritance, and polymorphism.

//POP (Procedural Programming):
//		
//		Centers around procedures or functions that operate on data. 
//		Data and functions are separate, and functions are designed to perform tasks or computations.
//		
//		Emphasizes a sequence of operations to be performed. 
//		It focuses on a linear flow of control and breaking down tasks into reusable procedures or functions.
